local lspconfig = require("lspconfig")

local servers = {
	"clangd",
	"vimls",
	"pyright",
	"bashls",
	"html",
	"jsonls",
	"yamlls",
	"prosemd_lsp",
}

for _, lsp in ipairs(servers) do
	lspconfig[lsp].setup({
		on_attach = on_attach,
		capabilities = capabilities,
		flags = lsp_flags,
	})
end

